shop = [['каретка', 1200], ['шатун', 1000], ['седло', 300],
        ['педаль', 100], ['седло', 1500], ['рама', 12000],
        ['обод', 2000], ['шатун', 200], ['седло', 2700]]

device = input('Название детали: ')
devices_data = [0, 0]

for i in shop:
    if i[0] == device:
        devices_data[0] += 1
        devices_data[1] += i[1]
print('Кол-во деталей —', devices_data[0], '\nОбщая стоимость —', devices_data[1])
